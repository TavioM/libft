void	trim_spaces(char *s)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	j = 0;
	k = 0;
	while (s[j])
	{
		if (s[j] != ' ')
		{
			s[i++] = s[j++];
			k = 1;
		}
		else if (k && s[j] == ' ')
		{
			s[i++] = s[j++];
			k = 0;
		}
		else
			j++;
	}
	s[i] = 0;
	if (i > 0 && s[i - 1] == ' ')
		s[i - 1] = 0;
}
