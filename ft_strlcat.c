#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	rt;

	i = 0;
	while (dst[i] && i < dstsize)
		i++;
	rt = i + ft_strlen(src);
	if (i == dstsize)
		return (rt);
	j = 0;
	while (src[j] && i + j + 1 < dstsize)
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = 0;
	return (rt);
}
